/**
 * Get a random conversation topic for the given proficiency level
 * Topics include all levels up to and including the user's level
 *
 * @param {string} level - CEFR level (A1, A2, B1, B2, C1, C2)
 * @returns {{ en: string, es: string }} - Random topic in both languages
 */
export function getRandomConversationTopic(level = "A1") {
  const levels = ["A1", "A2", "B1", "B2", "C1", "C2"];
  const levelIndex = levels.indexOf(level);
  const effectiveIndex = levelIndex === -1 ? 0 : levelIndex;

  // Collect all topics up to and including the user's level
  const availableTopics = [];
  for (let i = 0; i <= effectiveIndex; i++) {
    const levelTopics = conversationTopics[levels[i]];
    if (levelTopics) {
      availableTopics.push(...levelTopics);
    }
  }

  // Select a random topic
  const randomIndex = Math.floor(Math.random() * availableTopics.length);
  return availableTopics[randomIndex];
}

/**
 * Get a random conversation topic only from the user's current level
 *
 * @param {string} level - CEFR level (A1, A2, B1, B2, C1, C2)
 * @returns {{ en: string, es: string }} - Random topic in both languages
 */
export function getRandomTopicForLevel(level = "A1") {
  const topics = conversationTopics[level] || conversationTopics.A1;
  const randomIndex = Math.floor(Math.random() * topics.length);
  return topics[randomIndex];
}
