const normalizeKey = (value) =>
  String(value || "")
    .trim()
    .replace(/\s+/g, " ")
    .toLowerCase();

const withSourcePunctuation = (source, translation) => {
  if (!translation) return translation;
  let result = String(translation).trim();
  if (String(source).trim().endsWith("?") && !/[؟?]$/.test(result)) {
    result += "؟";
  } else if (String(source).trim().endsWith("!") && !/!$/.test(result)) {
    result += "!";
  } else if (String(source).trim().endsWith("...") && !/\.\.\.$/.test(result)) {
    result += "...";
  }
  return result;
};

