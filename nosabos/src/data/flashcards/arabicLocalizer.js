const translateToken = (token) => {
  const key = normalizeKey(token);
  return WORD_TRANSLATIONS[key] || CONNECTOR_TRANSLATIONS[key] || "";
};

const translateFragment = (text) => {
  const source = String(text || "").trim();
  if (!source) return "";

  const direct = EXACT_TRANSLATIONS[normalizeKey(source)];
  if (direct) return withSourcePunctuation(source, direct);

  const stripped = source.replace(/[?!]+$/g, "").replace(/\.\.\.$/g, "");
  const parts = stripped.split(/\s+/);
  const translatedParts = parts
    .map((part) => {
      const cleaned = part.replace(/^[^A-Za-z0-9]+|[^A-Za-z0-9]+$/g, "");
      const translated = translateToken(cleaned);
      if (!translated && cleaned) return "";
      return translated;
    })
    .filter(Boolean);

  if (!parts.length || translatedParts.length !== parts.filter(Boolean).length) {
    return "";
  }

  return withSourcePunctuation(source, translatedParts.join(" ").replace(/\s+/g, " ").trim());
};

const PATTERN_TRANSLATIONS = [
  [/^A table for (.+), please$/i, (count) => `ترابيزة لـ${translateFragment(count)}, من فضلك`],
  [/^Can I (.+)\?$/i, (rest) => `ممكن ${translateFragment(rest) || rest}؟`],
  [/^Can we (.+)\?$/i, (rest) => `ممكن ${translateFragment(rest) || rest}؟`],
  [/^Can you (.+)\?$/i, (rest) => `ممكن ${translateFragment(rest) || rest}؟`],
  [/^Could you (.+)\?$/i, (rest) => `ممكن ${translateFragment(rest) || rest}؟`],
  [/^Do you have (.+)\?$/i, (rest) => `عندك ${translateFragment(rest) || rest}؟`],
  [/^I have (.+)$/i, (rest) => `عندي ${translateFragment(rest) || rest}`],
  [/^I need (.+)$/i, (rest) => `محتاج ${translateFragment(rest) || rest}`],
  [/^I live in (.+)$/i, (rest) => `أنا ساكن في ${translateFragment(rest) || rest}`],
  [/^I'm (.+)$/i, (rest) => `أنا ${translateFragment(rest) || rest}`],
  [/^It was (.+)$/i, (rest) => `كان ${translateFragment(rest) || rest}`],
  [/^It's (.+)$/i, (rest) => `${translateFragment(rest) || rest}`],
  [/^Let's (.+)$/i, (rest) => `يلا ${translateFragment(rest) || rest}`],
  [/^What is (.+)\?$/i, (rest) => `${translateFragment(rest) || rest} إيه؟`],
  [/^Where is (.+)\?$/i, (rest) => `${translateFragment(rest) || rest} فين؟`],
  [/^Where's (.+)\?$/i, (rest) => `${translateFragment(rest) || rest} فين؟`],
  [/^Would you like to (.+)\?$/i, (rest) => `تحب ${translateFragment(rest) || rest}؟`],
];

export const translateFlashcardConceptToArabic = (englishText) => {
  if (!englishText || typeof englishText !== "string") return englishText;

  const source = englishText.trim();
  const normalized = normalizeKey(source);

  if (EXACT_TRANSLATIONS[normalized]) {
    return withSourcePunctuation(source, EXACT_TRANSLATIONS[normalized]);
  }

  for (const [pattern, formatter] of PATTERN_TRANSLATIONS) {
    const match = source.match(pattern);
    if (match) {
      const translated = formatter(...match.slice(1));
      if (translated) return translated;
    }
  }

  const fragment = translateFragment(source);
  if (fragment) return fragment;

  return source;
};

const addArabicConcept = (card) => {
  if (!card || typeof card !== "object") return card;
  if (!card.concept || typeof card.concept !== "object") return card;
  if (typeof card.concept.ar === "string") return card;
  return {
    ...card,
    concept: {
      ...card.concept,
      ar: translateFlashcardConceptToArabic(card.concept.en || card.concept.es),
    },
  };
};

export const withArabicFlashcardText = (cards = []) =>
  Array.isArray(cards) ? cards.map(addArabicConcept) : cards;
