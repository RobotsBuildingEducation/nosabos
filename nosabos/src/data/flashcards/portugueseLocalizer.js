const escapeRegex = (value) =>
  String(value || "").replace(/[.*+?^${}()|[\]\\]/g, "\\$&");

const OBVIOUS_SPANISH_LEAK_PATTERN = new RegExp(
  `(?:^|[^\\p{L}\\p{M}])(?:${SPANISH_LEAK_TOKENS.map(escapeRegex).join("|")})(?=$|[^\\p{L}\\p{M}])`,
  "iu",
);

const translateSpanishFlashcardText = (source) => {
  let translated = String(source || "").replace(/[¿¡]/g, "").trim();
  if (!translated) return "";

  SPANISH_SUBSTRING_REPLACEMENTS.forEach(([pattern, replacement]) => {
    translated = translated.replace(pattern, replacement);
  });

  translated = translated.replace(/\p{L}[\p{L}\p{M}]*/gu, (token) => {
    const replacement = SPANISH_TOKEN_TRANSLATIONS[normalizeKey(token)];
    return replacement ? applyTokenCase(token, replacement) : token;
  });

  return compactWhitespace(translated);
};

const getSafePortugueseCandidate = (source, candidate) => {
  if (typeof candidate !== "string") return "";
  const trimmed = compactWhitespace(candidate);
  if (!trimmed || OBVIOUS_SPANISH_LEAK_PATTERN.test(trimmed)) return "";
  return applySourceCase(source, trimmed);
};

export const translateFlashcardConceptToPortuguese = (
  englishText,
  spanishText = englishText,
) => {
  const source = spanishText || englishText;
  if (!source || typeof source !== "string") return englishText;

  const candidate =
    getSafePortugueseCandidate(
      englishText,
      PORTUGUESE_FLASHCARD_EN_EXACT[normalizeKey(englishText)],
    ) ||
    getSafePortugueseCandidate(
      source,
      PORTUGUESE_FLASHCARD_EXACT[normalizeKey(source)],
    ) ||
    getSafePortugueseCandidate(
      source,
      translateSpanishFlashcardText(source),
    ) ||
    getSafePortugueseCandidate(
      source,
      PORTUGUESE_FLASHCARD_CONCEPT_MAP[normalizeKey(source)],
    );

  if (candidate) return candidate;
  return englishText;
};

const addPortugueseConcept = (card) => {
  if (!card || typeof card !== "object") return card;
  if (
    card.concept &&
    typeof card.concept === "object" &&
    typeof card.concept.en === "string" &&
    typeof card.concept.es === "string" &&
    typeof card.concept.pt !== "string"
  ) {
    return {
      ...card,
      concept: {
        ...card.concept,
        pt: translateFlashcardConceptToPortuguese(
          card.concept.en,
          card.concept.es,
        ),
      },
    };
  }
  return card;
};

export const withPortugueseFlashcardText = (cards) =>
  Array.isArray(cards) ? cards.map(addPortugueseConcept) : cards;
