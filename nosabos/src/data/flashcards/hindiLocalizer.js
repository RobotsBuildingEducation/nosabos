const PATTERN_TRANSLATIONS = [
  [/^do you speak (.+)\?$/i, (_, lang) => `${translateFragment(lang)} बोलते हैं?`],
  [/^do you have (.+)\?$/i, (_, item) => `क्या आपके पास ${translateFragment(item)} है?`],
  [/^is there (.+)\?$/i, (_, item) => `क्या वहाँ ${translateFragment(item)} है?`],
  [/^where is (.+)\?$/i, (_, item) => `${translateFragment(item)} कहाँ है?`],
  [/^i need (.+)$/i, (_, item) => `मुझे ${translateFragment(item)} चाहिए`],
  [/^i have (.+)$/i, (_, item) => `मेरे पास ${translateFragment(item)} है`],
];

function singularize(value) {
  if (value.endsWith("ies")) return `${value.slice(0, -3)}y`;
  if (value.endsWith("es")) return value.slice(0, -2);
  if (value.endsWith("s")) return value.slice(0, -1);
  return value;
}

function translateWord(value) {
  const normalized = normalizeKey(value).replace(/[?!.,]/g, "");
  if (!normalized) return "";
  if (EXACT_TRANSLATIONS[normalized]) return EXACT_TRANSLATIONS[normalized];
  if (WORD_TRANSLATIONS[normalized]) return WORD_TRANSLATIONS[normalized];
  const singular = singularize(normalized);
  if (WORD_TRANSLATIONS[singular]) return WORD_TRANSLATIONS[singular];
  return "";
}

function translateFragment(value) {
  const source = String(value || "").trim();
  if (!source) return "";

  const exact = EXACT_TRANSLATIONS[normalizeKey(source)];
  if (exact) return exact;

  const normalized = normalizeKey(source).replace(/[?!.,]/g, "");
  if (!normalized.includes(" ")) {
    return translateWord(normalized) || "";
  }

  const tokens = normalized.split(" ");
  const translatedTokens = tokens.map((token) => {
    if (CONNECTOR_TRANSLATIONS[token]) return CONNECTOR_TRANSLATIONS[token];
    return translateWord(token);
  });

  return translatedTokens.every(Boolean) ? translatedTokens.join(" ") : "";
}

export const translateFlashcardConceptToHindi = (englishText) => {
  if (!englishText || typeof englishText !== "string") return englishText;

  const source = englishText.trim();
  const normalized = normalizeKey(source);

  if (EXACT_TRANSLATIONS[normalized]) {
    return applySourceCase(source, EXACT_TRANSLATIONS[normalized]);
  }

  for (const [pattern, translator] of PATTERN_TRANSLATIONS) {
    const match = source.match(pattern);
    if (match) {
      const translated = translator(...match);
      if (translated) return translated;
    }
  }

  const fragment = translateFragment(source);
  if (fragment) return applySourceCase(source, fragment);

  const transliterated = transliterateLatinToHindi(source);
  return transliterated && transliterated !== source ? transliterated : source;
};

const addHindiConcept = (card) => {
  if (!card || typeof card !== "object") return card;
  if (!card.concept || typeof card.concept !== "object") return card;
  if (typeof card.concept.hi === "string") return card;
  return {
    ...card,
    concept: {
      ...card.concept,
      hi: translateFlashcardConceptToHindi(card.concept.en || card.concept.es),
    },
  };
};

export const withHindiFlashcardText = (cards = []) =>
  Array.isArray(cards) ? cards.map(addHindiConcept) : cards;
