function singularize(value) {
  if (value.endsWith("ies")) return `${value.slice(0, -3)}y`;
  if (value.endsWith("es")) return value.slice(0, -2);
  if (value.endsWith("s")) return value.slice(0, -1);
  return value;
}

function translateWord(value) {
  const normalized = normalizeKey(value).replace(/[?!.,]/g, "");
  if (!normalized) return "";
  if (EXACT_TRANSLATIONS[normalized]) return EXACT_TRANSLATIONS[normalized];
  if (WORD_TRANSLATIONS[normalized]) return WORD_TRANSLATIONS[normalized];
  const singular = singularize(normalized);
  if (WORD_TRANSLATIONS[singular]) return WORD_TRANSLATIONS[singular];
  return "";
}

function translateFragment(value) {
  const source = String(value || "").trim();
  if (!source) return "";

  const exact = EXACT_TRANSLATIONS[normalizeKey(source)];
  if (exact) return exact;
  const normalized = normalizeKey(source).replace(/[?!.,]/g, "");
  if (!normalized.includes(" ")) {
    return translateWord(normalized) || "";
  }
  return "";
}

export const translateFlashcardConceptToJapanese = (englishText) => {
  if (!englishText || typeof englishText !== "string") return englishText;

  const source = englishText.trim();
  const normalized = normalizeKey(source);

  if (EXACT_TRANSLATIONS[normalized]) {
    return EXACT_TRANSLATIONS[normalized];
  }

  for (const [pattern, translator] of PATTERN_TRANSLATIONS) {
    const match = source.match(pattern);
    if (match) {
      const translated = translator(...match);
      if (translated && !/[A-Za-z]/.test(translated)) {
        return translated;
      }
    }
  }

  return translateFragment(source) || source;
};

const addJapaneseConcept = (card) => {
  if (!card || typeof card !== "object") return card;
  if (!card.concept || typeof card.concept !== "object") return card;
  if (typeof card.concept.ja === "string") return card;
  return {
    ...card,
    concept: {
      ...card.concept,
      ja: translateFlashcardConceptToJapanese(card.concept.en || card.concept.es),
    },
  };
};

export const withJapaneseFlashcardText = (cards = []) =>
  Array.isArray(cards) ? cards.map(addJapaneseConcept) : cards;
