export const translateFlashcardConceptToItalian = (englishText) => {
  if (!englishText || typeof englishText !== "string") return englishText;
  const translated = CONCEPT_TRANSLATIONS[normalizeKey(englishText)];
  return translated ? applySourceCase(englishText, translated) : englishText;
};

const addItalianConcept = (card) => {
  if (!card || typeof card !== "object") return card;
  if (
    card.concept &&
    typeof card.concept === "object" &&
    typeof card.concept.en === "string" &&
    typeof card.concept.es === "string" &&
    typeof card.concept.it !== "string"
  ) {
    return {
      ...card,
      concept: {
        ...card.concept,
        it: translateFlashcardConceptToItalian(card.concept.en),
      },
    };
  }
  return card;
};

export const withItalianFlashcardText = (cards) =>
  Array.isArray(cards) ? cards.map(addItalianConcept) : cards;
