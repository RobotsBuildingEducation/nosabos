const translateFallback = (source) => {
  const words = String(source || "").split(/(\b)/);
  const translated = words
    .map((part) => {
      const key = normalizeKey(part);
      return WORD_TRANSLATIONS[key] || part;
    })
    .join("")
    .replace(/\s+([.,;:!?])/g, "$1")
    .replace(/\s{2,}/g, " ")
    .trim();
  return translated && translated !== source ? translated : "Begriff";
};

export const translateFlashcardConceptToGerman = (englishText) => {
  if (!englishText || typeof englishText !== "string") return englishText;
  const translated = CONCEPT_TRANSLATIONS[normalizeKey(englishText)];
  return applySourceCase(
    englishText,
    translated || translateFallback(englishText),
  );
};

const addGermanConcept = (card) => {
  if (!card || typeof card !== "object") return card;
  if (
    card.concept &&
    typeof card.concept === "object" &&
    typeof card.concept.en === "string" &&
    typeof card.concept.es === "string" &&
    typeof card.concept.de !== "string"
  ) {
    return {
      ...card,
      concept: {
        ...card.concept,
        de: translateFlashcardConceptToGerman(card.concept.en),
      },
    };
  }
  return card;
};

export const withGermanFlashcardText = (cards = []) =>
  Array.isArray(cards) ? cards.map(addGermanConcept) : cards;
